public class FontBean {
    private int site;
    private String name;
    private String path;

    public int getSite() {
        return site;
    }

    public FontBean setSite(int site) {
        this.site = site;
        return this;
    }

    public String getName() {
        return name;
    }

    public FontBean setName(String name) {
        this.name = name;
        return this;
    }

    public String getPath() {
        return path;
    }

    public FontBean setPath(String path) {
        this.path = path;
        return this;
    }
}
